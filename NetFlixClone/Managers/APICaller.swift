    static let baseURL = "https://api.themoviedb.org"
}

// 열거형: - 관련된 값의 그룹을 정의함
// 제한된 선택지를 나타내는 데 사용됨
// API를 가져오는 데 실패한 경우 오류 처리 함
enum APIError: Error {
    case failTogetData
}


